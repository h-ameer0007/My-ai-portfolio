# math_ops.py
# Module for the cli calculator.
# contains available functions for the calculator.
import math

# defines add function


def add(num1, num2):
    return num1 + num2


# defines subtract function


def subtract(num1, num2):
    return num1-num2


# defines multiply function


def multiply(num1, num2):
    return num1 * num2


# defines decide function


def devide(num1, num2):
    return num1/num2

# imports the square root function form the maths module.


def square_root(root_number):
    return math.sqrt(root_number)
